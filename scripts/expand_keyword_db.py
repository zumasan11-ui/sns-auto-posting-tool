def quote_sheet_name(name: str) -> str:
    return "'" + name.replace("'", "''") + "'"


def col_letter(index: int) -> str:
    index += 1
    letters = ""
    while index:
        index, rem = divmod(index - 1, 26)
        letters = chr(65 + rem) + letters
    return letters


def clean(value: Any) -> str:
    return str(value or "").strip()


def parse_spreadsheet_id(value: str) -> str:
    value = value.strip()
    if "/spreadsheets/d/" in value:
        return value.split("/spreadsheets/d/", 1)[1].split("/", 1)[0]
    return value


def is_duplicate(candidate: str, existing: List[str]) -> bool:
    norm = normalize_keyword(candidate)
    if not norm:
        return True
    return any(normalize_keyword(item) == norm or is_same_meaning_keyword(candidate, item) for item in existing)


def build_column(existing: List[str], bank: List[str], target_min: int, target_max: int) -> tuple[List[str], List[str]]:
    result = [item for item in existing if clean(item)]
    added: List[str] = []
    for candidate in bank:
        if len(result) >= target_max:
            break
        if len(result) >= target_min and len(added) >= 0:
            break
        if not is_duplicate(candidate, result):
            result.append(candidate)
            added.append(candidate)
    if len(result) < target_min:
        for candidate in bank:
            if len(result) >= target_max:
                break
            if not is_duplicate(candidate, result):
                result.append(candidate)
                added.append(candidate)
    return result, added


def set_keyword_column_widths(service: Any, spreadsheet_id: str, sheet_name: str, sheet_id: int, headers: List[str]) -> None:
    requests = []
    for index, header in enumerate(headers):
        width = max(92, min(180, len(header) * 16 + 36))
        requests.append(
            {
                "updateDimensionProperties": {
                    "range": {"sheetId": sheet_id, "dimension": "COLUMNS", "startIndex": index, "endIndex": index + 1},
                    "properties": {"pixelSize": width},
                    "fields": "pixelSize",
                }
            }
        )
    if requests:
        service.spreadsheets().batchUpdate(spreadsheetId=spreadsheet_id, body={"requests": requests}).execute()


def main() -> int:
    parser = argparse.ArgumentParser(description="キーワードDBの既存ジャンル列を50〜100語程度まで拡充します。")
    parser.add_argument("--spreadsheet", default=os.getenv("AD_ANALYSIS_SPREADSHEET_ID", DEFAULT_SPREADSHEET_ID))
    parser.add_argument("--sheet", default=KEYWORD_SHEET)
    parser.add_argument("--target-min", type=int, default=50)
    parser.add_argument("--target-max", type=int, default=100)
    args = parser.parse_args()

    config = load_sheets_config()
    service = build_sheets_service(config)
    spreadsheet_id = parse_spreadsheet_id(args.spreadsheet)
    values = read_values(service, spreadsheet_id, f"{quote_sheet_name(args.sheet)}!A1:ZZ")
    if not values:
        raise RuntimeError("キーワードDBが空です。")

    headers = [clean(item) for item in values[0] if clean(item)]
    columns: Dict[str, List[str]] = {}
    added_by_genre: Dict[str, List[str]] = {}
    for col_index, header in enumerate(headers):
        existing = [clean(row[col_index]) for row in values[1:] if col_index < len(row) and clean(row[col_index])]
        bank = KEYWORD_BANK.get(header, [])
        if not bank:
            columns[header] = existing
            added_by_genre[header] = []
            continue
        expanded, added = build_column(existing, bank, args.target_min, args.target_max)
        columns[header] = expanded
        added_by_genre[header] = added

    max_len = max((len(items) for items in columns.values()), default=0)
    output = [headers]
    for row_index in range(max_len):
        output.append([columns[header][row_index] if row_index < len(columns[header]) else "" for header in headers])

    last_col = col_letter(len(headers) - 1)
    update_values(service, spreadsheet_id, f"{quote_sheet_name(args.sheet)}!A1:{last_col}{len(output)}", output, "RAW")

    spreadsheet = get_spreadsheet(service, spreadsheet_id)
    sheet_id = next(
        sheet["properties"]["sheetId"]
        for sheet in spreadsheet.get("sheets", [])
        if sheet.get("properties", {}).get("title") == args.sheet
    )
    set_keyword_column_widths(service, spreadsheet_id, args.sheet, sheet_id, headers)

    print("キーワードDB拡充結果")
    for header in headers:
        print(f"- {header}: {len(columns[header])}語（追加 {len(added_by_genre[header])}語）")
    total_added = sum(len(items) for items in added_by_genre.values())
    print(f"合計追加: {total_added}語")
    return 0


